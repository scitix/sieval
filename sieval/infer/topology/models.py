@dataclass(frozen=True)
class DeploymentPlan:
    """Complete deployment description -- the system's IR.

    Immutable. Generated by TopologyResolver, consumed by BackendTranslator.
    """

    checkpoint: str
    backend: str
    assignments: tuple[RoleAssignment, ...]
    attachments: tuple[ServiceBinding, ...] = ()
    deterministic: bool = False
    # Engine-level seed pinned under deterministic mode. User-supplied
    # `seed` / `random_seed` via `infer.overrides` takes precedence.
    # Always set (even when deterministic=False) so translators don't
    # need to guard against None; non-deterministic plans ignore it.
    seed: int = DETERMINISTIC_DEFAULT_SEED

    @property
    def is_disaggregated(self) -> bool:
        """True if the plan uses disaggregated roles (not just FULL)."""
        roles = {a.role for a in self.assignments}
        return roles != {WellKnownRole.FULL}

    @property
    def total_gpus(self) -> int:
        """Total GPU count across all assignments (including replicas)."""
        return sum(a.topology.gpu_count * a.replicas for a in self.assignments)

    def validate(self) -> list[str]:
        """Return list of validation errors (empty = valid)."""
        errors: list[str] = []
        if not self.assignments:
            errors.append("DeploymentPlan must have at least one assignment")
        for a in self.assignments:
            errors.extend(a.validate())
        # Cross-role: PD roles must be paired
        roles = {a.role for a in self.assignments}
        if WellKnownRole.PREFILL in roles and WellKnownRole.DECODE not in roles:
            errors.append("prefill role requires a matching decode role")
        if WellKnownRole.DECODE in roles and WellKnownRole.PREFILL not in roles:
            errors.append("decode role requires a matching prefill role")
        return errors


