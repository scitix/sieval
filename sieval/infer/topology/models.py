@dataclass(frozen=True)
class RoleAssignment:
    """Complete deployment description for one role.

    engine_params carries non-parallel parameters from recipe profiles
    (dtype, max_model_len, gpu_memory_utilization, etc.). Each role can
    have different engine_params -- e.g. PD split with different hardware
    or different memory budgets per role. The translator appends these
    as CLI flags alongside the parallel topology flags.

    __post_init__ normalizes engine_params keys to underscore form and
    raises ValueError if the input dict contains both dash and underscore
    variants of the same normalized key (e.g. ``{"foo-bar": 1, "foo_bar": 2}``).
    """

    role: str
    devices: DeviceGroup
    topology: ParallelTopology
    replicas: int = 1
    engine_params: Mapping[str, ParamValue] = field(default_factory=dict)
    scaling: ScalingPolicy | None = None  # Phase 6 placeholder

    def __post_init__(self) -> None:
        normalized = merge_params(self.engine_params)
        object.__setattr__(self, "engine_params", _freeze_dict(normalized))

    def validate(self) -> list[str]:
        """Return list of validation errors (empty = valid)."""
        errors = self.topology.validate()
        needed = self.topology.gpu_count * self.replicas
        if self.devices.count < needed:
            errors.append(
                f"Role {self.role}: needs {needed} GPUs "
                f"(tp={self.topology.tp} x dp={self.topology.dp} "
                f"x pp={self.topology.pp} x replicas={self.replicas}) "
                f"but DeviceGroup only has {self.devices.count}"
            )
        if self.replicas < 1:
            errors.append(f"Role {self.role}: replicas must be >= 1")
        return errors


