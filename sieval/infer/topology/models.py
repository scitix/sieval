@dataclass(frozen=True)
class ParallelTopology:
    """Parallel strategy for a set of GPUs.

    Each field maps to one parallelism dimension.
    EP reuses TP x DP devices (does not add GPU count).
    CP reuses TP-group devices.
    """

    tp: int = 1
    dp: int = 1
    pp: int = 1
    ep: int | None = None
    enable_dp_attention: bool = False
    cp: int | None = None

    @property
    def gpu_count(self) -> int:
        """Minimum GPU requirement (EP/CP reuse existing devices)."""
        return self.tp * self.dp * self.pp

    def validate(self) -> list[str]:
        """Return list of validation errors (empty = valid)."""
        errors: list[str] = []
        for name, val in [("tp", self.tp), ("dp", self.dp), ("pp", self.pp)]:
            if val < 1:
                errors.append(f"{name} must be >= 1, got {val}")
        if self.ep is not None and self.tp >= 1 and self.dp >= 1:
            if self.ep < 1:
                errors.append(f"ep must be >= 1, got {self.ep}")
            elif self.ep > self.tp * self.dp:
                errors.append(f"ep({self.ep}) > tp*dp({self.tp * self.dp})")
            elif self.ep <= self.tp and self.tp % self.ep != 0:
                # sglang requires TP % EP == 0 (computes tp_size // ep_size)
                errors.append(f"tp({self.tp}) % ep({self.ep}) != 0")
            elif self.ep > self.tp and self.ep % self.tp != 0:
                # When EP > TP, framework computes ep_size // tp_size
                errors.append(f"ep({self.ep}) % tp({self.tp}) != 0")
        if self.cp is not None and self.cp < 1:
            errors.append(f"cp must be >= 1, got {self.cp}")
        # NOTE: no DPA tp%dp constraint — sglang's actual constraint is
        # total_tp % dp == 0, and total_tp = tp * dp, so it is always satisfied.
        return errors


